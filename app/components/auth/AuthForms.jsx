export const Dashboards = () => {
  const router = useRouter();
  const [user, setUser] = useState(null); // Menyimpan data user
  const [loading, setLoading] = useState(true); // Menampilkan loading state
  const [error, setError] = useState(''); // Menampilkan error, jika ada

  useEffect(() => {
    const fetchUserData = async () => {
      try {
        const token = localStorage.getItem('token');
        if (!token) {
          throw new Error('No token found. Please log in.');
        }

        const res = await fetch('/api/auth/me', {
          method: 'GET',
          headers: {
            Authorization: `Bearer ${token}`,
          },
        });

        if (!res.ok) {
          const errorData = await res.json();
          throw new Error(errorData.message || 'Failed to fetch user data');
        }

        const data = await res.json();
        setUser(data); // Set data user
      } catch (err) {
        setError(err.message);
        localStorage.removeItem('token'); // Hapus token jika invalid
        router.push('/login'); // Redirect ke halaman login
      } finally {
        setLoading(false);
      }
    };

    fetchUserData();
  }, [router]);

  if (loading) {
    return <div>Loading...</div>; // Loading state
  }

  if (error) {
    return <div>Error: {error}</div>; // Tampilkan error jika ada
  }

  return (
    <div className="min-h-screen flex items-center justify-center bg-black-100">
      <div className="max-w-4xl w-full p-4 bg-white rounded-lg shadow-md">
        <h1 className="text-2xl font-bold mb-4 text-black">Welcome, {user.username}!</h1>
        <p className="text-gray-700 mb-4">Email: {user.email}</p>
        <button
          onClick={() => {
            localStorage.removeItem('token');
            router.push('/login');
          }}
          className="py-2 px-4 bg-red-600 text-white rounded-lg"
        >
          Logout
        </button>
      </div>
    </div>
  );
};